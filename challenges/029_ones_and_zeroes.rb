#!/usr/bin/ruby

bin = '01110101 01110011 01100101 00100000 01110111 ' +
      '01100101 01100100 01101110 01100101 01110011 ' +
      '01100100 01100001 01111001 00100000 01100110 ' +
      '01101111 01110010 00100000 01110100 01101000 ' +
      '01100101 00100000 01100001 01101110 01110011 ' +
      '01110111 01100101 01110010'

x = bin.split(' ')
str = ''

for i in 0...x.size
  hex = eval("0b#{x[i]}.to_i").chr
  str += hex
end

print str
